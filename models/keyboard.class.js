
/**
 * Class representing the keyboard controls.
 */
class Keyboard {
    /**
     * Indicates if the left arrow key is pressed.
     */
    LEFT = false;

    /**
     * Indicates if the right arrow key is pressed.
     */
    RIGHT = false;

    /**
     * Indicates if the up arrow key is pressed.
     */
    UP = false;

    /**
     * Indicates if the down arrow key is pressed.
     */
    DOWN = false;

    /**
     * Indicates if the space key is pressed.
     */
    SPACE = false;

    /**
     * Indicates if the D key is pressed.
     */
    D = false;

    /**
     * Indicates if the keyboard is in idle state.
     */
    idle = false;

    /**
     * Indicates if the keyboard is in long idle state.
     */
    long_idle = false;
}